            availablePhosphorus: template.availablePhosphorus,
            sodium: template.sodium,
            linoleicAcid: template.linoleicAcid,
            extraNutrients: template.extraNutrients,
            vitaminA: template.vitaminA,
            vitaminD3: template.vitaminD3,
            vitaminE: template.vitaminE,
            manganese: template.manganese,
            zinc: template.zinc,
