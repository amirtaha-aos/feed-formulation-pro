struct Ingredient: Identifiable, Hashable, Codable {
    let id: UUID
    var name: String
    var group: IngredientGroup
    var minPercent: Double
    var maxPercent: Double
    var pricePerKg: Double

    var crudeProtein: Double
    var metabolizableEnergy: Double
    var lysine: Double
    var methionine: Double
    var threonine: Double
    var calcium: Double
    var availablePhosphorus: Double
    var sodium: Double
    var linoleicAcid: Double
    var extraNutrients: [String: Double]

    var vitaminA: Double
    var vitaminD3: Double
    var vitaminE: Double

    var manganese: Double
    var zinc: Double
    var copper: Double
    var iron: Double
    var selenium: Double

    init(
        id: UUID = UUID(),
        name: String,
        group: IngredientGroup,
        minPercent: Double,
        maxPercent: Double,
        pricePerKg: Double,
        crudeProtein: Double,
        metabolizableEnergy: Double,
        lysine: Double,
        methionine: Double,
        threonine: Double,
        calcium: Double,
        availablePhosphorus: Double,
        sodium: Double,
        linoleicAcid: Double,
        extraNutrients: [String: Double] = [:],
        vitaminA: Double,
        vitaminD3: Double,
        vitaminE: Double,
        manganese: Double,
        zinc: Double,
        copper: Double,
        iron: Double,
        selenium: Double
    ) {
        self.id = id
        self.name = name
        self.group = group
        self.minPercent = minPercent
        self.maxPercent = maxPercent
        self.pricePerKg = pricePerKg
        self.crudeProtein = crudeProtein
        self.metabolizableEnergy = metabolizableEnergy
        self.lysine = lysine
        self.methionine = methionine
        self.threonine = threonine
        self.calcium = calcium
        self.availablePhosphorus = availablePhosphorus
        self.sodium = sodium
        self.linoleicAcid = linoleicAcid
        self.extraNutrients = extraNutrients
        self.vitaminA = vitaminA
        self.vitaminD3 = vitaminD3
        self.vitaminE = vitaminE
        self.manganese = manganese
        self.zinc = zinc
        self.copper = copper
        self.iron = iron
        self.selenium = selenium
    }

    enum CodingKeys: String, CodingKey {
        case id
        case name
        case group
        case minPercent
        case maxPercent
        case pricePerKg
        case crudeProtein
        case metabolizableEnergy
        case lysine
        case methionine
        case threonine
        case calcium
        case availablePhosphorus
        case sodium
        case linoleicAcid
        case extraNutrients
        case vitaminA
        case vitaminD3
        case vitaminE
        case manganese
        case zinc
        case copper
        case iron
        case selenium
    }

    init(from decoder: Decoder) throws {
        let container = try decoder.container(keyedBy: CodingKeys.self)
        id = try container.decode(UUID.self, forKey: .id)
        name = try container.decode(String.self, forKey: .name)
        group = try container.decode(IngredientGroup.self, forKey: .group)
        minPercent = try container.decode(Double.self, forKey: .minPercent)
        maxPercent = try container.decode(Double.self, forKey: .maxPercent)
        pricePerKg = try container.decode(Double.self, forKey: .pricePerKg)
        crudeProtein = try container.decode(Double.self, forKey: .crudeProtein)
        metabolizableEnergy = try container.decode(Double.self, forKey: .metabolizableEnergy)
        lysine = try container.decode(Double.self, forKey: .lysine)
        methionine = try container.decode(Double.self, forKey: .methionine)
        threonine = try container.decode(Double.self, forKey: .threonine)
        calcium = try container.decode(Double.self, forKey: .calcium)
        availablePhosphorus = try container.decode(Double.self, forKey: .availablePhosphorus)
        sodium = try container.decode(Double.self, forKey: .sodium)
        linoleicAcid = try container.decode(Double.self, forKey: .linoleicAcid)
        extraNutrients = try container.decodeIfPresent([String: Double].self, forKey: .extraNutrients) ?? [:]
        vitaminA = try container.decode(Double.self, forKey: .vitaminA)
        vitaminD3 = try container.decode(Double.self, forKey: .vitaminD3)
        vitaminE = try container.decode(Double.self, forKey: .vitaminE)
        manganese = try container.decode(Double.self, forKey: .manganese)
        zinc = try container.decode(Double.self, forKey: .zinc)
        copper = try container.decode(Double.self, forKey: .copper)
        iron = try container.decode(Double.self, forKey: .iron)
        selenium = try container.decode(Double.self, forKey: .selenium)
    }

    func encode(to encoder: Encoder) throws {
        var container = encoder.container(keyedBy: CodingKeys.self)
        try container.encode(id, forKey: .id)
        try container.encode(name, forKey: .name)
        try container.encode(group, forKey: .group)
        try container.encode(minPercent, forKey: .minPercent)
        try container.encode(maxPercent, forKey: .maxPercent)
        try container.encode(pricePerKg, forKey: .pricePerKg)
        try container.encode(crudeProtein, forKey: .crudeProtein)
        try container.encode(metabolizableEnergy, forKey: .metabolizableEnergy)
        try container.encode(lysine, forKey: .lysine)
        try container.encode(methionine, forKey: .methionine)
        try container.encode(threonine, forKey: .threonine)
        try container.encode(calcium, forKey: .calcium)
        try container.encode(availablePhosphorus, forKey: .availablePhosphorus)
        try container.encode(sodium, forKey: .sodium)
        try container.encode(linoleicAcid, forKey: .linoleicAcid)
        try container.encode(extraNutrients, forKey: .extraNutrients)
        try container.encode(vitaminA, forKey: .vitaminA)
        try container.encode(vitaminD3, forKey: .vitaminD3)
        try container.encode(vitaminE, forKey: .vitaminE)
        try container.encode(manganese, forKey: .manganese)
        try container.encode(zinc, forKey: .zinc)
        try container.encode(copper, forKey: .copper)
        try container.encode(iron, forKey: .iron)
        try container.encode(selenium, forKey: .selenium)
    }

    func value(for key: NutrientKey) -> Double {
        switch key {
        case .crudeProtein: return crudeProtein
        case .metabolizableEnergy: return metabolizableEnergy
        case .lysine: return lysine
        case .methionine: return methionine
        case .threonine: return threonine
        case .calcium: return calcium
        case .availablePhosphorus: return availablePhosphorus
        case .sodium: return sodium
        case .linoleicAcid: return linoleicAcid
        case .vitaminA: return vitaminA
        case .vitaminD3: return vitaminD3
        case .vitaminE: return vitaminE
        case .manganese: return manganese
        case .zinc: return zinc
        case .copper: return copper
        case .iron: return iron
        case .selenium: return selenium
        }
    }

