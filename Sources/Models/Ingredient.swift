    private static func applyWorkbookData(to library: [Ingredient]) -> [Ingredient] {
        var updated = library.map { ingredient in
            var copy = ingredient
            let canonicalName = workbookAliasByIngredientName[copy.name] ?? copy.name

            if canonicalName == "Fish Meal, 50% CP" || canonicalName == "Fish Meal, 70% CP" {
                if let base = workbookDataBank["Fish Meal, 60% CP"], let baseCP = base["CP"] {
                    let targetCP = canonicalName == "Fish Meal, 50% CP" ? 50.0 : 70.0
                    let scale = baseCP > 0 ? (targetCP / baseCP) : 1.0
                    var scaled = base.reduce(into: [String: Double]()) { result, item in
                        result[item.key] = item.value * scale
                    }
                    scaled["CP"] = targetCP
                    applyWorkbookValues(scaled, to: &copy)
                }
            } else if let data = workbookDataBank[canonicalName] {
                applyWorkbookValues(data, to: &copy)
            }
            return copy
        }

        let existingCanonicalNames = Set(
            updated.map { workbookAliasByIngredientName[$0.name] ?? $0.name }
        )
        for (name, data) in workbookDataBank.sorted(by: { $0.key < $1.key }) {
            guard !existingCanonicalNames.contains(name) else { continue }
            let group = inferredGroup(for: name)
            let maxPercent = inferredMaxPercent(for: group)
            var ingredient = Ingredient(
                name: name,
                group: group,
                minPercent: 0,
                maxPercent: maxPercent,
                pricePerKg: 0,
                crudeProtein: 0,
                metabolizableEnergy: 0,
                lysine: 0,
                methionine: 0,
                threonine: 0,
                calcium: 0,
                availablePhosphorus: 0,
                sodium: 0,
                linoleicAcid: 0,
                vitaminA: 0,
                vitaminD3: 0,
                vitaminE: 0,
                manganese: 0,
                zinc: 0,
                copper: 0,
                iron: 0,
                selenium: 0
            )
            applyWorkbookValues(data, to: &ingredient)
            updated.append(ingredient)
        }

        if let preset = workbookPricePresets.first(where: { $0.name == defaultWorkbookPricePresetName }) {
            updated = applyWorkbookPricePreset(preset, to: updated)
        }

        return updated
    }

    private static func applyWorkbookValues(_ data: [String: Double], to ingredient: inout Ingredient) {
        ingredient.extraNutrients = [:]
        for definition in workbookNutrientDefinitions {
            let value = data[definition.id] ?? 0
            if let key = workbookCoreKeyMap[definition.id] {
                switch key {
                case .crudeProtein: ingredient.crudeProtein = value
                case .metabolizableEnergy: ingredient.metabolizableEnergy = value
                case .lysine: ingredient.lysine = value
                case .methionine: ingredient.methionine = value
                case .threonine: ingredient.threonine = value
                case .calcium: ingredient.calcium = value
                case .availablePhosphorus: ingredient.availablePhosphorus = value
                case .sodium: ingredient.sodium = value
                case .linoleicAcid: ingredient.linoleicAcid = value
                case .vitaminA, .vitaminD3, .vitaminE, .manganese, .zinc, .copper, .iron, .selenium: break
                }
            } else if abs(value) > 1e-12 {
                ingredient.extraNutrients[definition.id] = value
            }
        }
    }

    static func applyWorkbookPricePreset(_ preset: WorkbookPricePreset, to library: [Ingredient]) -> [Ingredient] {
        library.map { ingredient in
            var copy = ingredient
            let canonicalName = workbookAliasByIngredientName[copy.name] ?? copy.name
            if let price = preset.prices[canonicalName] {
                copy.pricePerKg = price
            }
            return copy
        }
    }

    private static func inferredGroup(for name: String) -> IngredientGroup {
        let lower = name.lowercased()
        let grainWords = ["corn", "wheat", "barley", "oats", "rye", "rice", "sorghum", "peas", "linseeds"]
        if grainWords.contains(where: { lower.contains($0) }) {
            return .grain
        }
        if lower.contains("fish")
            || lower.contains("blood")
            || lower.contains("feather")
            || lower.contains("bone")
            || lower.contains("poultry")
            || lower.contains("hatchery")
        {
            return .animalSource
        }
        if lower.contains("meal")
            || lower.contains("bran")
            || lower.contains("ddgs")
            || lower.contains("byproduct")
            || lower.contains("middlings")
            || lower.contains("gluten feed")
            || lower.contains("germ")
            || lower.contains("hulls")
            || lower.contains("pulp")
            || lower.contains("molasses")
        {
            return .plantByProducts
        }
        if lower.contains("premix")
            || lower.contains("vitamin")
            || lower.contains("mineral")
            || lower.contains("phytase")
            || lower.contains("chloride")
            || lower.contains("bicarbonate")
            || lower.contains("carbonate")
            || lower.contains("salt")
            || lower.contains("oil")
            || lower.contains("lysine")
            || lower.contains("methionine")
            || lower.contains("threonine")
            || lower.contains("tryptophan")
            || lower.contains("valine")
            || lower.contains("isoleucine")
            || lower.contains("arginine")
        {
            return .supplementsAdditives
        }
        return .userFeed
    }

    private static func inferredMaxPercent(for group: IngredientGroup) -> Double {
        switch group {
        case .grain:
            return 60
        case .protein:
            return 40
        case .plantByProducts:
            return 28
        case .animalSource:
            return 10
        case .fat:
            return 8
        case .mineral:
            return 5
        case .supplementsAdditives:
            return 6
        case .userFeed:
            return 10
        }
    }

    private static func appendUnique(
        names: [String],
        template: Ingredient,
        group: IngredientGroup,
        maxPercent: Double,
        pricePerKg: Double,
        seen: inout Set<String>,
        output: inout [Ingredient]
    ) {
        for name in names {
            let key = name.lowercased()
            guard !seen.contains(key) else { continue }
            seen.insert(key)
            output.append(
                cloneIngredient(
                    name: name,
                    from: template,
                    group: group,
                    minPercent: 0,
                    maxPercent: maxPercent,
                    pricePerKg: pricePerKg
                )
            )
        }
    }

